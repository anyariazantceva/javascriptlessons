// Using the above operands apply different JavaScript arithmetic operators
let operandOne = 4;
let operandTwo = 3;

let sum = operandOne + operandTwo;
let sub = operandOne - operandTwo;
let mult = operandOne * operandTwo;
let div = operandTwo / operandTwo;
let mod = operandOne % operandTwo;
let inc = operandOne += operandTwo;
let dec = operandOne -= operandTwo;